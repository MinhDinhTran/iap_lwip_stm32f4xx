/**
  * @brief  output information to the uart device in a prescribed format
  * @param  Pointer to format string, followed by the arguments for conversion
  * @retval >= 0: Number of bytes which have been stored in the "Up"-buffer.
  *         < 0:  Error
  */
void UART_Send(uint8_t *pbBuff, uint16_t wLength)
{
    if(pbBuff != NULL)
    {
        while(__HAL_UART_GET_FLAG(&huart5, UART_FLAG_TC) == RESET);
        memcpy(gabUartTxBuffer, pbBuff, wLength);
        HAL_UART_Transmit_DMA(&huart5, gabUartTxBuffer, wLength);
    }
}
